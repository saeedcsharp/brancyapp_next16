interface FontMap {
  [key: string]: {
    [key: string]: string;
  };
}
type FontState = {
  isHidden: boolean;
  inputText: string;
  selectedFont: string;
  copied: string;
  convertedText: string;
};

type FontAction =
  | { type: "TOGGLE_HIDDEN" }
  | { type: "SET_INPUT_TEXT"; payload: string }
  | { type: "SET_SELECTED_FONT"; payload: string }
  | { type: "SET_COPIED"; payload: string }
  | { type: "SET_CONVERTED_TEXT"; payload: string }
  | { type: "RESET_COPIED" };

const fontReducer = (state: FontState, action: FontAction): FontState => {
  switch (action.type) {
    case "TOGGLE_HIDDEN":
      return { ...state, isHidden: !state.isHidden };
    case "SET_INPUT_TEXT":
      return { ...state, inputText: action.payload };
    case "SET_SELECTED_FONT":
      return { ...state, selectedFont: action.payload };
    case "SET_COPIED":
      return { ...state, copied: action.payload };
    case "SET_CONVERTED_TEXT":
      return { ...state, convertedText: action.payload };
    case "RESET_COPIED":
      return { ...state, copied: "" };
    default:
      return state;
  }
};
const convertTextHelper = (text: string, style: string, map: FontMap): string => {
  return text
    .split("")
    .map((char) => map[style]?.[char] || map[style]?.[char.toLowerCase()] || char)
    .join("");
};

